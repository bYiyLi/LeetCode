class MinStack155 {

    /** initialize your data structure here. */
    int[] content ;
    int n = 16;
    int top = 0;
    public MinStack155() {
        content = new int[n];
    }

    public void push(int val) {
        top++;
        if (top<n){
            n+=4;
            int [] tem = new int[n];
            System.arraycopy(content,0,tem,0,content.length);
            content = tem;
        }
        content[top-1]=val;
    }

    public void pop() {
        top--;
    }

    public int top() {
        return content[top-1];
    }

    public int getMin() {
        int min =content[0];
        for (int i = 0; i < top; i++) {
            if (min>content[i]){
                min = content[i];
            }
        }
        return min;
    }
}

